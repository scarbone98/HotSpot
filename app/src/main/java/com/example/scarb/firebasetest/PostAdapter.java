package com.example.scarb.firebasetest;

import android.content.Context;
import android.support.annotation.LayoutRes;
import android.support.annotation.NonNull;
import android.support.annotation.Nullable;
import android.view.View;
import android.view.ViewGroup;
import android.widget.ArrayAdapter;

import java.util.List;

/**
 * Created by scarbone on 5/11/17.
 */

public class PostAdapter extends ArrayAdapter<PostData> {

    public PostAdapter(@NonNull Context context, @LayoutRes int resource, @NonNull List<PostData> objects) {
        super(context, resource, objects);
    }

    @NonNull
    @Override
    public View getView(int position, @Nullable View convertView, @NonNull ViewGroup parent) {
        return super.getView(position, convertView, parent);
    }
}
